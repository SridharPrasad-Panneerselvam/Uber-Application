    private void btnConsolidatedSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsolidatedSearchActionPerformed
        // TODO add your handling code here:
        templist.removeAll(templist);

        for (Car car : carInformationList.carInformationList) {
            //System.out.println((YesMainCert.isSelected()&&car.isMaintenance_certificate())||(NoMainCert.isSelected()&&!car.isMaintenance_certificate())||(NAmaincert.isSelected()));
            if ((txtBrand.getText().equals(car.getBrand()) || car.getBrand().equals(""))
                    && (txtModelNo.getText().equals(car.getModel_num())
                    || txtModelNo.getText().equals(""))
                    && (txtSerialNo.getText().equals(car.getSerial_num() + "")
                    || txtSerialNo.getText().equals(""))
                    && (txtAvailableCity.getText().equals(car.getAvailble_city())
                    || txtAvailableCity.getText().equals(""))
                    && ((!txtManufacturedYear1.getText().isEmpty()
                    && Integer.parseInt(txtManufacturedYear1.getText())
                    == car.getManufactured_year()) || txtManufacturedYear1.getText().isEmpty())
                    && (txtAvailableCity.getText().equals(car.getAvailble_city()) || txtAvailableCity.getText().equals("") )
                    && ( (btnYesCA.isSelected() == true &&
                        btnNoCA.isSelected() == false 
                    && btnYesCA.isSelected() == car.isAvailable())||(
                        btnNoCA.isSelected() == true &&
                        btnYesCA.isSelected() ==false && 
                    btnNoCA.isSelected() == !car.isAvailable())
                     || (btnYesCA.isSelected()==false && btnNoCA.isSelected() == false)) 
                    &&
                    ( (btnYesMC.isSelected() == true &&
                        btnNoMC.isSelected() == false 
                    && btnYesMC.isSelected() == car.isMaintenance_certificate())||(
                        btnNoMC.isSelected() == true &&
                        btnYesMC.isSelected() ==false && 
                    btnNoMC.isSelected() == !car.isMaintenance_certificate())
                     || (btnYesMC.isSelected()==false && btnNoMC.isSelected() == false))                 
                     &&
                    ((Integer.parseInt(String.valueOf(maxSeatComboBox.getSelectedItem())) >= car.getMax_seats()) ||
                    (Integer.parseInt(String.valueOf(minSeatComboBox.getSelectedItem())) <= car.getMin_seats())))
                    {
                templist.add(car);
            }

        }
        populateTable(templist);
        
    }//GEN-LAST:event_btnConsolidatedSearchActionPerformed

