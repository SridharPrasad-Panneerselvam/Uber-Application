    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) searchTable.getModel();
        dtm.setRowCount(0);

        for (Car car : carInformationList.getCarList()) {

            Object row[] = new Object[6];
            row[0] = car;
            row[1] = car.getModel_num();
            row[2] = car.getMin_seats();
            row[3] = car.getMax_seats();;
            row[4] = car.getAvailble_city();
            row[5] = car.getManufactured_year();
            dtm.addRow(row);
        }
    }

