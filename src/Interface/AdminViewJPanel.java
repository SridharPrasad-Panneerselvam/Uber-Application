    private void SearchManufacturedYearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchManufacturedYearActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dt = (DefaultTableModel) searchTable.getModel();
        dt.setRowCount(0);
        int year = Integer.parseInt(txtManufacturedYear.getText());
        for (Car car : carInformationList.getCarList()) {
            if (car.getManufactured_year() == year) {
            Object row[] = new Object[6];
            row[0] = car;
            row[1] = car.getModel_num();
            row[2] = car.getMin_seats();
            row[3] = car.getMax_seats();;
            row[4] = car.getAvailble_city();
            row[5] = car.getManufactured_year();
            dt.addRow(row);
            }
        }
        txtManufacturedYear.setText("");
    }//GEN-LAST:event_SearchManufacturedYearActionPerformed

